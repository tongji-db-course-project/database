CREATE TABLE point_record (
    point_record_id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id       NUMBER(10) NOT NULL,
    sale_id         NUMBER(10),
    change_type     VARCHAR2(20) NOT NULL,
    change_points   NUMBER(10) NOT NULL,
    remain_points   NUMBER(10) NOT NULL,
    record_time     DATE DEFAULT SYSDATE,
    remark          VARCHAR2(200),
    CONSTRAINT fk_pr_member FOREIGN KEY (member_id) REFERENCES member(member_id),
    CONSTRAINT fk_pr_sale FOREIGN KEY (sale_id) REFERENCES sale_order(sale_id),
    CONSTRAINT ck_pr_type CHECK (change_type IN ('增加','扣减','抵现'))
);

COMMENT ON TABLE point_record IS '积分流水表：记录会员积分的每一次增减变动历史';
COMMENT ON COLUMN point_record.sale_id IS '关联销售单，记录是哪笔交易产生的积分变动；若是其他活动导致积分变化时可为空';

CREATE TABLE supplier_settlement (
    settlement_id     NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    supplier_id       NUMBER(10) NOT NULL,
    purchase_id       NUMBER(10) NOT NULL,
    settlement_date   DATE DEFAULT SYSDATE,
    settlement_amount NUMBER(12, 2) NOT NULL,
    paid_amount       NUMBER(12, 2) DEFAULT 0,
    unpaid_amount     NUMBER(12, 2) NOT NULL,
    status            VARCHAR2(20) NOT NULL,
    remark            VARCHAR2(200),
    CONSTRAINT fk_ss_supplier FOREIGN KEY (supplier_id) REFERENCES supplier(supplier_id),
    CONSTRAINT fk_ss_purchase FOREIGN KEY (purchase_id) REFERENCES purchase_order(order_id),
    CONSTRAINT ck_ss_status CHECK (status IN ('未结算', '已结算', '部分结算'))
);

COMMENT ON TABLE supplier_settlement IS '供应商结算表：管理与供应商的财务结账情况';
