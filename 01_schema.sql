CREATE TABLE sys_role (
    role_id    NUMBER(10) PRIMARY KEY,
    role_name  VARCHAR2(50) NOT NULL UNIQUE,
    role_desc  VARCHAR2(200)
);

COMMENT ON TABLE sys_role IS '角色表';
COMMENT ON COLUMN sys_role.role_id IS '角色编号';
COMMENT ON COLUMN sys_role.role_name IS '角色名称';
COMMENT ON COLUMN sys_role.role_desc IS '角色说明';

CREATE TABLE sys_menu (
    menu_id     NUMBER(10) PRIMARY KEY,
    menu_name   VARCHAR2(50) NOT NULL,
    menu_url    VARCHAR2(100),
    parent_id   NUMBER(10),
    menu_order  NUMBER(5)
);

COMMENT ON TABLE sys_menu IS '菜单功能表';
COMMENT ON COLUMN sys_menu.menu_id IS '菜单编号';
COMMENT ON COLUMN sys_menu.menu_name IS '菜单名称';
COMMENT ON COLUMN sys_menu.menu_url IS '菜单路径';
COMMENT ON COLUMN sys_menu.parent_id IS '上级菜单编号';
COMMENT ON COLUMN sys_menu.menu_order IS '菜单顺序';

CREATE TABLE sys_user (
    user_id     NUMBER(10) PRIMARY KEY,
    role_id     NUMBER(10),
    username    VARCHAR2(50) NOT NULL UNIQUE,
    password    VARCHAR2(100) NOT NULL,
    real_name   VARCHAR2(50),
    gender      VARCHAR2(10),
    phone       VARCHAR2(20),
    status      VARCHAR2(20),
    create_time DATE DEFAULT SYSDATE,
    CONSTRAINT fk_user_role FOREIGN KEY (role_id) REFERENCES sys_role(role_id)
);

COMMENT ON TABLE sys_user IS '用户表';
COMMENT ON COLUMN sys_user.user_id IS '用户编号';
COMMENT ON COLUMN sys_user.role_id IS '角色编号';
COMMENT ON COLUMN sys_user.username IS '登录账号';
COMMENT ON COLUMN sys_user.password IS '登录密码';
COMMENT ON COLUMN sys_user.real_name IS '真实姓名';
COMMENT ON COLUMN sys_user.gender IS '性别';
COMMENT ON COLUMN sys_user.phone IS '联系电话';
COMMENT ON COLUMN sys_user.status IS '用户状态';
COMMENT ON COLUMN sys_user.create_time IS '创建时间';

CREATE TABLE sys_role_menu (
    role_menu_id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_id      NUMBER(10) NOT NULL,
    menu_id      NUMBER(10) NOT NULL,
    CONSTRAINT fk_rm_role FOREIGN KEY (role_id) REFERENCES sys_role(role_id) ON DELETE CASCADE,
    CONSTRAINT fk_rm_menu FOREIGN KEY (menu_id) REFERENCES sys_menu(menu_id) ON DELETE CASCADE,
    CONSTRAINT uk_role_menu UNIQUE (role_id, menu_id)
);

COMMENT ON TABLE sys_role_menu IS '权限中间表：定义哪些角色可以访问哪些菜单';

CREATE TABLE supplier (
    supplier_id    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    supplier_name  VARCHAR2(100) NOT NULL,
    contact_name   VARCHAR2(50),
    phone          VARCHAR2(20),
    email          VARCHAR2(100),
    address        VARCHAR2(200),
    credit_level   VARCHAR2(20),
    payment_cycle  NUMBER(5),
    CONSTRAINT pk_supplier PRIMARY KEY (supplier_id)
);

COMMENT ON TABLE supplier IS '供应商信息表';
COMMENT ON COLUMN supplier.supplier_id IS '供应商唯一标识 (主键)';
COMMENT ON COLUMN supplier.credit_level IS '供应商信誉等级';
COMMENT ON COLUMN supplier.payment_cycle IS '约定结算周期（天数）';

CREATE TABLE product_category (
    category_id   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name VARCHAR2(50) NOT NULL UNIQUE,
    category_desc VARCHAR2(200),
    status        VARCHAR2(20) DEFAULT '启用'
);

COMMENT ON TABLE product_category IS '商品类别表';
COMMENT ON COLUMN product_category.category_name IS '类别名称，如食品、日用品等';

CREATE TABLE member (
    member_id      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    member_name    VARCHAR2(100) NOT NULL,
    gender         VARCHAR2(10) CHECK (gender IN ('男', '女', '未知')),
    phone          VARCHAR2(20) NOT NULL,
    birthday       DATE,
    points         NUMBER(10) DEFAULT 0,
    total_amount   NUMBER(12, 2) DEFAULT 0.00,
    level_name     VARCHAR2(20) DEFAULT '普通会员',
    create_time    DATE DEFAULT SYSDATE,
    CONSTRAINT pk_member PRIMARY KEY (member_id),
    CONSTRAINT uk_member_phone UNIQUE (phone)
);

COMMENT ON TABLE member IS '会员信息表';
COMMENT ON COLUMN member.member_id IS '会员唯一标识 (主键)';
COMMENT ON COLUMN member.phone IS '会员手机号 (唯一约束)';
COMMENT ON COLUMN member.points IS '当前剩余积分';
COMMENT ON COLUMN member.level_name IS '会员等级（硬编码文本）';

CREATE TABLE product (
    product_id     NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id    NUMBER(10) NOT NULL,
    supplier_id    NUMBER(10) NOT NULL,
    product_name   VARCHAR2(100) NOT NULL,
    barcode        VARCHAR2(50) UNIQUE,
    specification  VARCHAR2(100),
    purchase_price NUMBER(10, 2) DEFAULT 0,
    sale_price     NUMBER(10, 2) DEFAULT 0,
    stock_warning  NUMBER(10) DEFAULT 10,
    unit           VARCHAR2(20),
    status         VARCHAR2(20) DEFAULT '在售',
    CONSTRAINT fk_prod_category FOREIGN KEY (category_id) REFERENCES product_category(category_id),
    CONSTRAINT fk_prod_supplier FOREIGN KEY (supplier_id) REFERENCES supplier(supplier_id)
);

COMMENT ON TABLE product IS '商品基础资料表';

CREATE TABLE purchase_order (
    order_id        NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    order_code      VARCHAR2(20) NOT NULL,
    supplier_id     NUMBER(10),
    purchase_date   DATE DEFAULT TRUNC(SYSDATE),
    total_amount    NUMBER(12, 2),
    applicant_id    NUMBER(10),
    approver_id     NUMBER(10),
    status          VARCHAR2(20) DEFAULT '待审批',
    CONSTRAINT pk_purchase_order PRIMARY KEY (order_id),
    CONSTRAINT uk_purchase_order_code UNIQUE (order_code),
    CONSTRAINT fk_po_supplier FOREIGN KEY (supplier_id) REFERENCES supplier(supplier_id),
    CONSTRAINT fk_po_applicant FOREIGN KEY (applicant_id) REFERENCES sys_user(user_id),
    CONSTRAINT fk_po_approver FOREIGN KEY (approver_id) REFERENCES sys_user(user_id),
    CONSTRAINT ck_po_status CHECK (status IN ('待审批', '已审批', '已入库', '已作废'))
);

COMMENT ON TABLE purchase_order IS '采购订单主表';
COMMENT ON COLUMN purchase_order.order_id IS '采购订单唯一标识 (主键)';
COMMENT ON COLUMN purchase_order.order_code IS '采购单据编码 (系统生成, 唯一)';
COMMENT ON COLUMN purchase_order.supplier_id IS '供应商ID (关联supplier表)';
COMMENT ON COLUMN purchase_order.applicant_id IS '申请用户ID (关联sys_user表)';
COMMENT ON COLUMN purchase_order.approver_id IS '审批用户ID (关联sys_user表)';
COMMENT ON COLUMN purchase_order.status IS '单据状态';

CREATE TABLE purchase_order_detail (
